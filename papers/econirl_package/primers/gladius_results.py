#!/usr/bin/env python3
"""GLADIUS Primer — auto-generate results for the LaTeX document.

Runs GLADIUS and NFXP on the Rust bus engine. GLADIUS recovers
replacement cost within 8% using neural Q-values with linear projection,
demonstrating that neural IRL can extract interpretable structural
parameters on a tabular MDP where ground truth is available.

Usage:
    cd /path/to/econirl
    .venv/bin/python papers/econirl_package/primers/gladius/gladius_results.py
"""

import json
import time
from pathlib import Path

import jax.numpy as jnp
import numpy as np

OUT = Path(__file__).resolve().parent / "gladius_results.tex"

N_BINS = 90
BETA = 0.95
TRUE_OC = 0.01
TRUE_RC = 3.0
N_INDIVIDUALS = 500
N_PERIODS = 100
SEED = 42


def main():
    from econirl.environments.rust_bus import RustBusEnvironment
    from econirl.estimation.gladius import GLADIUSEstimator, GLADIUSConfig
    from econirl.estimation.nfxp import NFXPEstimator
    from econirl.preferences.linear import LinearUtility

    n_obs = N_INDIVIDUALS * N_PERIODS
    print(f"GLADIUS Primer — generating results")
    print(f"  Rust bus, {N_BINS} bins, beta={BETA}, {n_obs:,} obs")

    env = RustBusEnvironment(
        operating_cost=TRUE_OC, replacement_cost=TRUE_RC,
        num_mileage_bins=N_BINS, discount_factor=BETA,
    )
    panel = env.generate_panel(n_individuals=N_INDIVIDUALS, n_periods=N_PERIODS, seed=SEED)
    utility = LinearUtility.from_environment(env)
    problem = env.problem_spec
    transitions = env.transition_matrices

    results = {}

    # ── NFXP ──
    print("\n  Running NFXP...")
    t0 = time.time()
    nfxp = NFXPEstimator(
        optimizer="BHHH", inner_solver="hybrid",
        inner_tol=1e-10, inner_max_iter=100000,
        se_method="robust", verbose=False,
    )
    nr = nfxp.estimate(panel=panel, utility=utility, problem=problem, transitions=transitions)
    results["nfxp"] = {
        "oc": float(nr.parameters[0]), "rc": float(nr.parameters[1]),
        "ll": float(nr.log_likelihood), "time": time.time() - t0,
    }
    print(f"    oc={results['nfxp']['oc']:.6f}, rc={results['nfxp']['rc']:.4f}")

    # ── GLADIUS ──
    print("\n  Running GLADIUS...")
    t0 = time.time()
    gladius = GLADIUSEstimator(config=GLADIUSConfig(
        q_hidden_dim=64, q_num_layers=2,
        v_hidden_dim=64, v_num_layers=2,
        max_epochs=300, batch_size=512,
        bellman_penalty_weight=1.0,
        compute_se=False, verbose=False,
    ))
    gr = gladius.estimate(panel=panel, utility=utility, problem=problem, transitions=transitions)
    results["gladius"] = {
        "oc": float(gr.parameters[0]), "rc": float(gr.parameters[1]),
        "ll": float(gr.log_likelihood), "time": time.time() - t0,
        "rc_pct_err": abs(float(gr.parameters[1]) - TRUE_RC) / TRUE_RC * 100,
    }
    # Check for projection R2 in metadata
    if hasattr(gr, 'metadata') and gr.metadata and 'projection_r2' in gr.metadata:
        results["gladius"]["r2"] = gr.metadata["projection_r2"]
    print(f"    oc={results['gladius']['oc']:.6f}, rc={results['gladius']['rc']:.4f}, "
          f"rc_err={results['gladius']['rc_pct_err']:.1f}%")

    # ── Write LaTeX ──
    def fmt(v, d=6):
        return f"{v:.{d}f}" if v is not None else "---"

    tex = []
    tex.append("% Auto-generated by gladius_results.py — do not edit by hand")
    tex.append(f"% Rust bus, {N_BINS} bins, beta={BETA}, {n_obs} obs, seed={SEED}")
    tex.append("")
    tex.append(f"\\newcommand{{\\gladNbins}}{{{N_BINS}}}")
    tex.append(f"\\newcommand{{\\gladBeta}}{{{BETA}}}")
    tex.append(f"\\newcommand{{\\gladNobs}}{{{n_obs:,}}}")
    tex.append(f"\\newcommand{{\\gladTrueOC}}{{{TRUE_OC}}}")
    tex.append(f"\\newcommand{{\\gladTrueRC}}{{{TRUE_RC}}}")
    tex.append("")
    tex.append(f"\\newcommand{{\\gladNfxpOC}}{{{fmt(results['nfxp']['oc'])}}}")
    tex.append(f"\\newcommand{{\\gladNfxpRC}}{{{fmt(results['nfxp']['rc'], 4)}}}")
    tex.append(f"\\newcommand{{\\gladNfxpLL}}{{{fmt(results['nfxp']['ll'], 2)}}}")
    tex.append(f"\\newcommand{{\\gladNfxpTime}}{{{fmt(results['nfxp']['time'], 1)}}}")
    tex.append("")
    tex.append(f"\\newcommand{{\\gladOC}}{{{fmt(results['gladius']['oc'])}}}")
    tex.append(f"\\newcommand{{\\gladRC}}{{{fmt(results['gladius']['rc'], 4)}}}")
    tex.append(f"\\newcommand{{\\gladLL}}{{{fmt(results['gladius']['ll'], 2)}}}")
    tex.append(f"\\newcommand{{\\gladTime}}{{{fmt(results['gladius']['time'], 1)}}}")
    tex.append(f"\\newcommand{{\\gladRCerr}}{{{results['gladius']['rc_pct_err']:.1f}}}")
    r2 = results['gladius'].get('r2')
    tex.append(f"\\newcommand{{\\gladRsq}}{{{fmt(r2, 3) if r2 else '---'}}}")
    tex.append("")

    tex.append("\\begin{table}[h!]")
    tex.append("\\centering\\small")
    tex.append(f"\\caption{{GLADIUS versus NFXP on Rust bus "
               f"(\\gladNbins\\ states, $\\beta = \\gladBeta$, \\gladNobs\\ obs). "
               f"GLADIUS recovers $RC$ within \\gladRCerr\\% via neural Q-values "
               f"with action-difference projection. "
               f"True: $\\theta_c = \\gladTrueOC$, $RC = \\gladTrueRC$.}}")
    tex.append("\\label{tab:gladius_results}")
    tex.append("\\begin{tabular*}{\\textwidth}{@{\\extracolsep{\\fill}} l r r}")
    tex.append("\\toprule")
    tex.append("Metric & NFXP & GLADIUS \\\\")
    tex.append("\\midrule")
    tex.append("$\\hat\\theta_c$ & \\gladNfxpOC & \\gladOC \\\\")
    tex.append("$\\hat{RC}$ & \\gladNfxpRC & \\gladRC \\\\")
    tex.append("Log-likelihood & $\\gladNfxpLL$ & $\\gladLL$ \\\\")
    tex.append("Time (s) & \\gladNfxpTime & \\gladTime \\\\")
    tex.append("\\bottomrule")
    tex.append("\\end{tabular*}")
    tex.append("\\end{table}")

    OUT.write_text("\n".join(tex) + "\n")
    print(f"\n  Wrote {OUT}")
    OUT.with_suffix(".json").write_text(json.dumps(results, indent=2))
    print(f"  Wrote {OUT.with_suffix('.json')}")


if __name__ == "__main__":
    main()
