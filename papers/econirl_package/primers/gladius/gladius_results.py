#!/usr/bin/env python3
"""GLADIUS Primer — auto-generate results for the LaTeX document.

Runs GLADIUS on the multi-component bus (400 states) where NFXP is
slow due to the large state space and the transition tensor is
2 x 400 x 400. GLADIUS trains neural Q-values without needing to
store or invert this tensor.

Usage:
    cd /path/to/econirl
    .venv/bin/python papers/econirl_package/primers/gladius/gladius_results.py
"""

import json
import time
from pathlib import Path

import jax.numpy as jnp
import numpy as np

OUT = Path(__file__).resolve().parent / "gladius_results.tex"

K_COMPONENTS = 2
M_BINS = 20
BETA = 0.99
N_INDIVIDUALS = 500
N_PERIODS = 100
SEED = 42


def main():
    from econirl.environments.multi_component_bus import MultiComponentBusEnvironment
    from econirl.estimation.gladius import GLADIUSEstimator, GLADIUSConfig
    from econirl.estimation.nfxp import NFXPEstimator
    from econirl.preferences.linear import LinearUtility

    n_states = M_BINS ** K_COMPONENTS
    n_obs = N_INDIVIDUALS * N_PERIODS
    print(f"GLADIUS Primer — generating results")
    print(f"  Multi-component bus: K={K_COMPONENTS}, M={M_BINS} -> {n_states} states")
    print(f"  Data: {n_obs:,} obs, beta={BETA}")

    env = MultiComponentBusEnvironment(K=K_COMPONENTS, M=M_BINS, discount_factor=BETA)
    panel = env.generate_panel(n_individuals=N_INDIVIDUALS, n_periods=N_PERIODS, seed=SEED)
    utility = LinearUtility(
        feature_matrix=env.feature_matrix,
        parameter_names=env.parameter_names,
    )
    problem = env.problem_spec
    transitions = env.transition_matrices
    true_params = env.get_true_parameter_vector()

    results = {"n_states": n_states, "true_params": [float(p) for p in true_params]}

    # ── NFXP ──
    print("\n  Running NFXP...")
    t0 = time.time()
    nfxp = NFXPEstimator(
        inner_solver="hybrid", inner_tol=1e-10, inner_max_iter=100000,
        se_method="robust", verbose=False,
    )
    nr = nfxp.estimate(panel=panel, utility=utility, problem=problem, transitions=transitions)
    nfxp_time = time.time() - t0
    results["nfxp"] = {
        "rc": float(nr.parameters[0]), "oc": float(nr.parameters[1]),
        "ll": float(nr.log_likelihood), "time": nfxp_time,
    }
    print(f"    params={[f'{p:.4f}' for p in nr.parameters]}, time={nfxp_time:.1f}s")

    # ── GLADIUS ──
    print("\n  Running GLADIUS...")
    t0 = time.time()
    gladius = GLADIUSEstimator(config=GLADIUSConfig(
        q_hidden_dim=64, q_num_layers=2,
        v_hidden_dim=64, v_num_layers=2,
        max_epochs=500, batch_size=512,
        compute_se=False, verbose=False,
    ))
    gr = gladius.estimate(panel=panel, utility=utility, problem=problem, transitions=transitions)
    gladius_time = time.time() - t0
    results["gladius"] = {
        "rc": float(gr.parameters[0]), "oc": float(gr.parameters[1]),
        "ll": float(gr.log_likelihood), "time": gladius_time,
    }
    print(f"    params={[f'{p:.4f}' for p in gr.parameters]}, time={gladius_time:.1f}s")

    def fmt(v, d=4):
        return f"{v:.{d}f}" if v is not None else "---"

    tex = []
    tex.append("% Auto-generated by gladius_results.py — do not edit by hand")
    tex.append(f"% Multi-component bus, K={K_COMPONENTS}, M={M_BINS}, {n_states} states")
    tex.append("")
    tex.append(f"\\newcommand{{\\gladNstates}}{{{n_states}}}")
    tex.append(f"\\newcommand{{\\gladBeta}}{{{BETA}}}")
    tex.append(f"\\newcommand{{\\gladNobs}}{{{n_obs:,}}}")
    tex.append(f"\\newcommand{{\\gladKcomp}}{{{K_COMPONENTS}}}")
    tex.append(f"\\newcommand{{\\gladMbins}}{{{M_BINS}}}")
    tex.append("")
    tex.append(f"\\newcommand{{\\gladNfxpRC}}{{{fmt(results['nfxp']['rc'])}}}")
    tex.append(f"\\newcommand{{\\gladNfxpOC}}{{{fmt(results['nfxp']['oc'], 6)}}}")
    tex.append(f"\\newcommand{{\\gladNfxpLL}}{{{fmt(results['nfxp']['ll'], 2)}}}")
    tex.append(f"\\newcommand{{\\gladNfxpTime}}{{{fmt(results['nfxp']['time'], 1)}}}")
    tex.append("")
    tex.append(f"\\newcommand{{\\gladRC}}{{{fmt(results['gladius']['rc'])}}}")
    tex.append(f"\\newcommand{{\\gladOC}}{{{fmt(results['gladius']['oc'], 6)}}}")
    tex.append(f"\\newcommand{{\\gladLL}}{{{fmt(results['gladius']['ll'], 2)}}}")
    tex.append(f"\\newcommand{{\\gladTime}}{{{fmt(results['gladius']['time'], 1)}}}")
    tex.append("")

    tex.append("\\begin{table}[h!]")
    tex.append("\\centering\\small")
    tex.append(f"\\caption{{Multi-component bus (\\gladKcomp\\ components, "
               f"\\gladMbins\\ bins, \\gladNstates\\ states, $\\beta = \\gladBeta$). "
               f"NFXP needs the full $(2, 400, 400)$ transition tensor. "
               f"GLADIUS learns from raw transitions without storing $P$.}}")
    tex.append("\\label{tab:gladius_results}")
    tex.append("\\begin{tabular*}{\\textwidth}{@{\\extracolsep{\\fill}} l r r}")
    tex.append("\\toprule")
    tex.append("Metric & NFXP & GLADIUS \\\\")
    tex.append("\\midrule")
    tex.append("$\\hat\\theta_{\\text{rc}}$ & \\gladNfxpRC & \\gladRC \\\\")
    tex.append("$\\hat\\theta_{\\text{oc}}$ & \\gladNfxpOC & \\gladOC \\\\")
    tex.append("Log-likelihood & $\\gladNfxpLL$ & $\\gladLL$ \\\\")
    tex.append("Time (s) & \\gladNfxpTime & \\gladTime \\\\")
    tex.append("Needs $P$ tensor & Yes & No \\\\")
    tex.append("\\bottomrule")
    tex.append("\\end{tabular*}")
    tex.append("\\end{table}")

    OUT.write_text("\n".join(tex) + "\n")
    print(f"\n  Wrote {OUT}")
    OUT.with_suffix(".json").write_text(json.dumps(results, indent=2))


if __name__ == "__main__":
    main()
